int
sys_setprio(void)
{
  int n;
  if(argint(0, &n) < 0)
  {
    cprintf("\ninvalid syntax, arguement needed\n\n");
    return -1;
  }
  if(n<=0 || n>100)
  {
    cprintf("\npriority must lie in range [1, %d]\n\n",MAX_PRIORITY);
    return -2;
  }
  acquire(&ptable.lock);
  //cprintf("old priority: %d\n",proc->priority);
  proc->priority = n;
  //cprintf("new priority: %d\n",proc->priority);
  release(&ptable.lock);
  return n;  
}
