void
scheduler(void)
{
  struct proc *p;
  struct proc *max_p; //process corresponding to max_run needed
  int max_run = -1;    //max_run time based on priority and carry(based on sleeping time)
  int proc_found=0;  //1 if we find any process to run else false;

  for(;;){
    // Enable interrupts on this processor.
    sti();
    
    max_run = -1;
    proc_found = 0;

    // Loop over process table looking for process to run.
    acquire(&ptable.lock);
    for(p = ptable.proc; p < &ptable.proc[NPROC]; p++){
      if(p->state != RUNNABLE || p->has_run != 0)          //continue as the process is not runnable
        continue;
      if(p->priority + p->carry > max_run)
      {
        max_run=p->priority + p->carry;
        max_p = p;
        proc_found = 1;    //found atleast one process which is runnable
      }
    }

    if(proc_found==0)   //no process found, all process has completed one round, so make has_run to 0 again
    {
      for(p = ptable.proc; p < &ptable.proc[NPROC]; p++)p->has_run=0;
    }

    if(proc_found==1)
    {
      // Switch to chosen process.  It is the process's job
      // to release ptable.lock and then reacquire it
      // before jumping back to us.
      proc = max_p;
      switchuvm(max_p);
      max_p->state = RUNNING;
      max_p->carry = max_p->carry + max_p->priority;      //set up run time
      if(max_p->carry > MAX_PRIORITY*2)max_p->carry = MAX_PRIORITY*2;  //max run
      swtch(&cpu->scheduler, max_p->context);
      switchkvm();

      // Process is done running for now.
      // It should have changed its p->state before coming back.
      proc = 0;
    }
    release(&ptable.lock);

  }
}
